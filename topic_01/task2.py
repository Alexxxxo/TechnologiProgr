str = input("Enter str: ")

print(str.strip("&")) # removes & characters if they appear at the front of the string and at the end
print(str.capitalize()) # returns a copy of the string and the first letter of the word in uppercase
print(str.title()) # returns a copy of the string, the first letters of the strings will be lowercase, the rest are not
print(str.upper()) # returns upper case strings
print(str.lower()) # returns lower case strings